    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel tabel = (DefaultTableModel) jTable1.getModel();
        tabel.setRowCount(0); // Clearing all rows before adding new ones

        try {
            java.util.Date tgl = jDateChooser1.getDate();
            java.util.Date tgl2 = jDateChooser2.getDate();

            String insertSql = "SELECT pembelian.No_Pembelian, pembelian.Kode_Barang, pembelian.Nama_Barang, pembelian.Jumlah_Beli, pembelian.Tanggal_Beli, pembelian.Kode_Suplier, barang.Stok, barang.Saham FROM pembelian INNER JOIN barang ON barang.Kode_Barang = pembelian.Kode_Barang";

            // Check if both dates are empty or null
            if (tgl == null && tgl2 == null) {
                // No date inputs, so fetch all data
                insertSql = "SELECT pembelian.No_Pembelian, pembelian.Kode_Barang, pembelian.Nama_Barang, pembelian.Jumlah_Beli, pembelian.Tanggal_Beli, pembelian.Kode_Suplier, barang.Stok, barang.Saham FROM pembelian INNER JOIN barang ON barang.Kode_Barang = pembelian.Kode_Barang";
            } else {
                // Date inputs are provided, include date filter
                insertSql += " WHERE pembelian.Tanggal_Beli BETWEEN ? AND ?";
            }

            java.sql.Connection conn = (Connection) Pertemuan1.Config.configDB();
            java.sql.PreparedStatement insertPat = conn.prepareStatement(insertSql);

            // Set parameters only if dates are provided
            if (tgl != null && tgl2 != null) {
                insertPat.setDate(1, new java.sql.Date(tgl.getTime()));
                insertPat.setDate(2, new java.sql.Date(tgl2.getTime()));
            }

            java.sql.ResultSet rs = insertPat.executeQuery();

            while (rs.next()) {
                tabel.addRow(new Object[]{
                    rs.getString(1), rs.getString(2), rs.getString(3),
                    rs.getString(4), rs.getString(5), rs.getString(6),
                    rs.getString(7), rs.getString(8)
                });
            }

            jTable1.setModel(tabel);

        } catch (SQLException e) {
            System.out.println("Error: " + e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       try {
            java.util.Date startDate = jDateChooser1.getDate();
            java.util.Date endDate = jDateChooser2.getDate();

            String insertSql = "SELECT pembelian.No_Pembelian, pembelian.Kode_Barang, pembelian.Nama_Barang, pembelian.Jumlah_Beli, pembelian.Tanggal, pembelian.Kode_Suplier, barang.Stok, barang.Saham FROM pembelian INNER JOIN barang ON barang.Kode_Barang = pembelian.Kode_Barang";

            java.sql.Connection conn = (Connection) Pertemuan1.Config.configDB();
            java.sql.PreparedStatement insertPat;

            if (startDate != null && endDate != null) {
                insertSql += " WHERE pembelian.Tanggal_Beli BETWEEN ? AND ?";
                insertPat = conn.prepareStatement(insertSql);

                insertPat.setDate(1, new java.sql.Date(startDate.getTime()));
                insertPat.setDate(2, new java.sql.Date(endDate.getTime()));
            } else {
                insertPat = conn.prepareStatement(insertSql);
            }

            java.sql.ResultSet rs = insertPat.executeQuery();

            if (!rs.isBeforeFirst()) {
                System.out.println("No data found for the given parameters.");
            } else {
                String report = "C:\\Users\\Asus\\Documents\\NetBeansProjects\\JavaProject1\\src\\Pertemuan1\\Laporan\\LaporanPembelian.jrxml";

                JasperReport JRpt = JasperCompileManager.compileReport(report);
                HashMap hash = new HashMap();

                if (startDate != null && endDate != null) {
                    hash.put("startDate", new java.sql.Date(startDate.getTime()));
                    hash.put("endDate", new java.sql.Date(endDate.getTime()));
                }

                JasperPrint JPrint = JasperFillManager.fillReport(JRpt, hash, conn);

                JasperViewer.viewReport(JPrint, false);
            }
        } catch (SQLException | JRException e) {
  
        }

        jDateChooser1.setDate(null);
        jDateChooser2.setDate(null);

    }//GEN-LAST:event_jButton2ActionPerformed

