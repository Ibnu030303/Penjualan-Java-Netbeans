    public void hitung () {
        try {
            String query = "SELECT COUNT(*) AS TotalPembelian, SUM(pembelian.Jumlah_Beli) AS TotalJumlahBeli, SUM(barang.Stok) AS TotalStok, SUM(barang.Saham) AS TotalSaham FROM pembelian INNER JOIN barang ON barang.Kode_Barang = pembelian.Kode_Barang";

            java.sql.Connection conn = (Connection) Pertemuan1.Config.configDB();
            java.sql.PreparedStatement queryPat = conn.prepareStatement(query);
            java.sql.ResultSet rs = queryPat.executeQuery();

            if (rs.next()) {
                int totalPembelian = rs.getInt("TotalPembelian");
                int totalJumlahBeli = rs.getInt("TotalJumlahBeli");
                int totalStok = rs.getInt("TotalStok");
                int totalSaham = rs.getInt("TotalSaham");

                String Pembelian = String.valueOf(totalPembelian); 
                jTextField1.setText(Pembelian);
                String JumlahBeli = String.valueOf(totalJumlahBeli); 
                jTextField2.setText(JumlahBeli);
                String Stok = String.valueOf(totalStok); 
                jTextField3.setText(Stok);
                String Saham = String.valueOf(totalSaham); 
                jTextField4.setText(Saham);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "error");
        }
    }

